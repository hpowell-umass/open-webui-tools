class TestArxivTool(unittest.TestCase):
    def setUp(self):
        self.tool = Tools()

    def test_paper_searching(self):
        """Test paper searching returns valid formatted results."""
        results = self.tool.search_arxiv("attention is all you need", max_results=3)
        self.assertIsInstance(results, str)
        self.assertIn("Attention Is All You Need", results)
        self.assertIn("1706.03762", results)
        self.assertGreater(len(results), 200)

    def test_equation_conversion_inline_and_block(self):
        """Test pandoc retains proper $...$ and $$...$$ syntax (independent of full paper)."""
        sample_latex = r"""
\documentclass{article}
\begin{document}
Inline equation: $E = mc^2$.

Block equation:
\[
\int_0^\infty e^{-x^2} \, dx = \frac{\sqrt{\pi}}{2}
\]
\end{document}
"""
        with tempfile.TemporaryDirectory() as tmp:
            tex_path = os.path.join(tmp, "test.tex")
            with open(tex_path, "w", encoding="utf-8") as f:
                f.write(sample_latex)
            
            try:
                proc = subprocess.run(
                    ["pandoc", "-f", "latex", "-t", "markdown", "--standalone", tex_path],
                    cwd=tmp,
                    capture_output=True,
                    text=True,
                    timeout=10,
                )
                self.assertEqual(proc.returncode, 0)
                md = proc.stdout
                self.assertIn("$E = mc^2$", md)          # inline preserved
                self.assertIn("$$", md)                   # display uses $$
                self.assertIn(r"\int_0^\infty e^{-x^2}", md)
            except FileNotFoundError:
                self.skipTest("pandoc not available in test environment")
            except Exception as e:
                self.fail(f"Equation conversion test failed: {e}")

    def test_full_markdown_conversion_actual_latex_paper(self):
        """Test full conversion on a real modern LaTeX paper (multi-file capable)."""
        md = self.tool.get_paper_markdown("1706.03762")  # Attention Is All You Need
        self.assertIsInstance(md, str)
        self.assertGreater(len(md), 5000)
        self.assertIn("# Attention Is All You Need", md)
        self.assertIn("## Abstract", md)
        # Check equation preservation (paper has many)
        self.assertTrue("$" in md or "$$" in md)
        self.assertIn("Transformer", md)

    def test_pre_latex_pdf_paper(self):
        """Test PDF fallback on an older paper (many pre-~2000 have limited/no source)."""
        # 1993 cond-mat paper (source often unavailable or minimal)
        md = self.tool.get_paper_markdown("cond-mat/9301011")
        self.assertIsInstance(md, str)
        self.assertGreater(len(md), 1000)
        self.assertIn("# ", md)  # title present
        self.assertIn("## Abstract", md)
        # PDF fallback indicator
        self.assertTrue("Page 1" in md or "extracted from PDF" in md.lower())

    def test_non_existent_paper(self):
        """Test graceful handling of invalid/non-existent arXiv ID."""
        md = self.tool.get_paper_markdown("9999.99999")
        self.assertIsInstance(md, str)
        self.assertIn("Paper not found", md)
        self.assertIn("9999.99999", md)



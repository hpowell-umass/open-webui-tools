import arxiv
import requests
import tarfile
import tempfile
import os
import subprocess
from pathlib import Path
import fitz  # PyMuPDF
from typing import List, Optional, Dict
import unittest
from unittest.mock import patch


class Tools:
    def __init__(self):
        """Initialize the ArXiv tool. Citation enabled for visibility in Open-WebUI."""
        self.citation = True

    def search_arxiv(self, query: str, max_results: int = 10) -> str:
        """
        Search arXiv for papers matching the query.

        :param query: Search query (e.g., "transformer attention mechanism" or author:arxiv_id)
        :param max_results: Maximum number of results to return (default 10, max 50)
        :return: Formatted string of matching papers with ID, title, authors, abstract snippet, and PDF link.
        """
        try:
            search = arxiv.Search(
                query=query,
                max_results=max_results,
                sort_by=arxiv.SortCriterion.Relevance,
            )
            results: List[arxiv.Result] = list(search.results())
            
            if not results:
                return "No papers found for the query."
            
            output = f"**Found {len(results)} papers for query: '{query}'**\n\n"
            for i, result in enumerate(results, 1):
                authors = ", ".join([author.name for author in result.authors])
                abstract_snippet = result.summary[:300] + "..." if len(result.summary) > 300 else result.summary
                arxiv_id = result.get_short_id()  # e.g., 1706.03762
                output += f"{i}. **{result.title}**\n"
                output += f"   ID: `{arxiv_id}`\n"
                output += f"   Authors: {authors}\n"
                output += f"   Published: {result.published.strftime('%Y-%m-%d')}\n"
                output += f"   Abstract: {abstract_snippet}\n"
                output += f"   PDF: {result.pdf_url}\n"
                output += f"   Source: https://arxiv.org/src/{arxiv_id}\n\n"
            
            return output.strip()
        
        except Exception as e:
            return f"Error searching arXiv: {str(e)}"

    def get_paper_markdown(self, arxiv_id: str) -> str:
        """
        Convert an arXiv paper to Markdown. Prioritizes LaTeX source (single or multi-file with \input{}).
        Falls back to PDF parsing for pre-LaTeX/old papers without source.

        :param arxiv_id: arXiv ID (e.g., "1706.03762" or "2305.12345")
        :return: Full Markdown of the paper (title, authors, abstract + body). Equations preserved as $...$ (inline) and $$...$$ (display).
        """
        # Step 1: Fetch metadata (always available)
        try:
            search = arxiv.Search(id_list=[arxiv_id])
            result = next(search.results())
            title = result.title
            authors = ", ".join([a.name for a in result.authors])
            abstract = result.summary
            published = result.published.strftime("%Y-%m-%d")
        except StopIteration:
            return f"❌ Paper not found: {arxiv_id}. Please check the ID and try again."
        except Exception as e:
            return f"❌ Error fetching metadata for {arxiv_id}: {str(e)}"

        header = f"# {title}\n\n"
        header += f"**Authors:** {authors}  \n"
        header += f"**Published:** {published}  \n"
        header += f"**arXiv ID:** `{arxiv_id}`  \n\n"
        header += f"## Abstract\n\n{abstract}\n\n"
        header += "## Full Content\n\n"

        # Step 2: Try LaTeX source (handles single .tex or multi-file with \input{})
        with tempfile.TemporaryDirectory() as tmp_dir:
            source_url = f"https://arxiv.org/src/{arxiv_id}"
            try:
                resp = requests.get(source_url, stream=True, timeout=30)
                if resp.status_code == 200:
                    content_type = resp.headers.get("content-type", "").lower()
                    extracted = False
                    
                    # Handle tar.gz (most common for multi-file)
                    if "tar" in content_type or "gzip" in content_type or source_url.endswith((".tar.gz", ".tgz")):
                        try:
                            with tarfile.open(fileobj=resp.raw, mode="r:gz") as tar:
                                tar.extractall(path=tmp_dir)
                            extracted = True
                        except tarfile.TarError:
                            pass
                    
                    # Fallback for rare single .tex.gz or plain .tex
                    elif "text" in content_type or source_url.endswith(".tex"):
                        tex_path = os.path.join(tmp_dir, f"{arxiv_id}.tex")
                        with open(tex_path, "wb") as f:
                            f.write(resp.content)
                        extracted = True
                    
                    if extracted:
                        # Find main .tex (looks for \documentclass first, then any .tex)
                        main_tex = self._find_main_tex(tmp_dir)
                        if main_tex:
                            try:
                                # pandoc compiles the full document (resolves \input{} relative to cwd)
                                proc = subprocess.run(
                                    [
                                        "pandoc",
                                        "-f", "latex",
                                        "-t", "markdown",
                                        "--standalone",
                                        "--wrap=none",
                                        main_tex,
                                    ],
                                    cwd=tmp_dir,
                                    capture_output=True,
                                    text=True,
                                    timeout=60,
                                    check=False,
                                )
                                if proc.returncode == 0:
                                    body = proc.stdout
                                    # pandoc guarantees proper $...$ (inline) and $$...$$ (display) syntax
                                    return header + body
                                else:
                                    body = f"⚠️ Pandoc conversion warning (return code {proc.returncode}):\n{proc.stderr}"
                            except FileNotFoundError:
                                body = "⚠️ pandoc not found on system. Install pandoc to enable LaTeX → Markdown."
                            except subprocess.TimeoutExpired:
                                body = "⚠️ LaTeX conversion timed out (paper too large/complex)."
                            except Exception as e:
                                body = f"⚠️ LaTeX conversion error: {str(e)}"
                        else:
                            body = "⚠️ No .tex file found in source archive."
                    else:
                        body = "⚠️ Could not parse source archive."
                else:
                    body = "⚠️ No LaTeX source available (pre-2000 or source-restricted paper)."
            except requests.RequestException:
                body = "⚠️ Failed to download LaTeX source."

            # Step 3: PDF fallback (PyMuPDF text extraction)
            if "No LaTeX" in body or "⚠️" in body or "failed" in body.lower():
                pdf_url = f"https://arxiv.org/pdf/{arxiv_id}.pdf"
                try:
                    pdf_resp = requests.get(pdf_url, stream=True, timeout=30)
                    if pdf_resp.status_code == 200:
                        pdf_path = os.path.join(tmp_dir, "paper.pdf")
                        with open(pdf_path, "wb") as f:
                            for chunk in pdf_resp.iter_content(chunk_size=8192):
                                f.write(chunk)
                        
                        doc = fitz.open(pdf_path)
                        body = ""
                        for i, page in enumerate(doc):
                            text = page.get_text("text")
                            body += f"### Page {i+1}\n\n{text.strip()}\n\n"
                        doc.close()
                        body = f"**Note:** This paper had no LaTeX source. Text extracted from PDF (equations may appear as plain text).\n\n{body}"
                    else:
                        body = "❌ Could not download PDF."
                except Exception as e:
                    body = f"❌ PDF fallback failed: {str(e)}"

            return header + body

    def _find_main_tex(self, directory: str) -> Optional[str]:
        """Helper: Find the primary .tex file (contains \documentclass) or fallback to first .tex."""
        tex_files = list(Path(directory).rglob("*.tex"))
        if not tex_files:
            return None
        
        for tex_path in tex_files:
            try:
                with open(tex_path, "r", encoding="utf-8", errors="ignore") as f:
                    head = f.read(2000)
                    if "\\documentclass" in head:
                        return str(tex_path)
            except Exception:
                continue
        
        # Fallback
        return str(tex_files[0])


